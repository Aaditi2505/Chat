# requirements: pandas, sklearn, streamlit, gtts, playsound, gradio, whisper

# ------------------------------
# ğŸ§  Model and Data Preparation
# ------------------------------
import pandas as pd
from sklearn.linear_model import LogisticRegression
from sklearn.preprocessing import LabelEncoder
from sklearn.feature_extraction.text import TfidfVectorizer

df = pd.read_csv("kamaraj_college_faq.csv")
df.dropna(inplace=True)

le = LabelEncoder()
df["Answer_Label"] = le.fit_transform(df["Answer"])

vectorizer = TfidfVectorizer()
X = vectorizer.fit_transform(df["Question"])
y = df["Answer_Label"]

model = LogisticRegression()
model.fit(X, y)

# ------------------------------
# ğŸ”Š Text-to-Speech (gTTS-based fallback)
# ------------------------------
try:
    import pyttsx3

    def speak_text(text):
        engine = pyttsx3.init()
        engine.setProperty('rate', 150)
        engine.say(text)
        engine.runAndWait()

except ImportError:
    from gtts import gTTS
    import playsound
    import os

    def speak_text(text):
        tts = gTTS(text=text, lang='en')
        filename = "temp_voice.mp3"
        tts.save(filename)
        playsound.playsound(filename)
        os.remove(filename)

# ------------------------------
# ğŸ§ Whisper Voice Recognition
# ------------------------------
import whisper
whisper_model = whisper.load_model("base")

# ------------------------------
# ğŸ¤– Gradio Voice+Text Chatbot
# ------------------------------
import gradio as gr

def chatbot(audio=None, text=None):
    if audio is not None:
        result = whisper_model.transcribe(audio)
        user_input = result["text"]
    elif text:
        user_input = text
    else:
        return "â— Please ask a question."

    vec = vectorizer.transform([user_input])
    prediction = model.predict(vec)[0]
    answer = le.inverse_transform([prediction])[0]

    speak_text(answer)

    return f"ğŸ—£ï¸ You asked: {user_input}\n\nâœ… Answer: {answer}"

def launch_gradio():
    iface = gr.Interface(
        fn=chatbot,
        inputs=[
            gr.Audio(sources=["microphone"], type="filepath", label="ğŸ¤ Speak your question"),
            gr.Textbox(lines=2, placeholder="Or type your question here", label="ğŸ“ Text question")
        ],
        outputs="text",
        title="ğŸ“ Kamaraj College FAQ - Voice + Text Chatbot",
        description="Ask via microphone or type. It will answer and speak back.",
    )
    iface.launch()

# ------------------------------
# ğŸ–¥ï¸ Streamlit Text UI
# ------------------------------
import streamlit as st

def launch_streamlit():
    st.set_page_config(page_title="Kamaraj College FAQ Chatbot", layout="centered")
    st.title("ğŸ“ Kamaraj College FAQ Chatbot")
    st.markdown("Ask anything related to **Kamaraj College of Engineering and Technology**!")

    user_question = st.text_input("ğŸ’¬ Type your question here:")

    if st.button("ğŸ” Get Answer"):
        if not user_question.strip():
            st.warning("âš ï¸ Please enter a question.")
        else:
            user_vector = vectorizer.transform([user_question])
            predicted_label = model.predict(user_vector)[0]
            predicted_answer = le.inverse_transform([predicted_label])[0]
            st.success(f"ğŸŸ¢ **Answer:** {predicted_answer}")
            speak_text(predicted_answer)

# ------------------------------
# ğŸ¯ Execution Entry Point
# ------------------------------
if __name__ == "__main__":
    import sys
    if "gradio" in sys.argv:
        launch_gradio()
    else:
        launch_streamlit()
